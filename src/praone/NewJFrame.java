    private void startPCButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startPCButton5ActionPerformed
        int theNumber;
        DefaultTableModel model = (DefaultTableModel) mTable5.getModel();
        model.setRowCount(0);
        try
        {   
            
            theNumber = Integer.parseInt(this.TextField5.getText()); 
            if (theNumber < 0 || theNumber > 100)
                throw new Exception();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Введите натуральное число");
            return;
        }
        if (this.isPrimitive5.isSelected())
        {
            int guess;
            int min = 0;
            int max = 100;
            do
            {
                guess = ThreadLocalRandom.current().nextInt(min, max );
                if (guess > theNumber)
                {
                    model.addRow(new Object[]{guess, "Ваше число больше!"});
                    max = guess;
                }
                if (guess < theNumber)
                {
                    model.addRow(new Object[]{guess, "Ваше число меньше!"});
                    min = guess + 1;
                }
            } while (guess != theNumber);
            model.addRow(new Object[]{guess, "В точку!"});    
            
        } else
        {
            int guess;
            int min = 0, max = 100;
            do
            {
                guess = (min + max) / 2;
                if (guess > theNumber)
                {
                    model.addRow(new Object[]{guess, "Ваше число больше!"});
                    max = guess;
                }
                if (guess < theNumber)
                {
                    model.addRow(new Object[]{guess, "Ваше число меньше!"});
                    min = guess + 1;
                }
            } while (guess != theNumber);
            model.addRow(new Object[]{guess, "В точку!"});
        }
    }//GEN-LAST:event_startPCButton5ActionPerformed

