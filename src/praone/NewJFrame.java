    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int row = jTable1.getSelectedRow();
        int column = jTable1.getSelectedColumn();
        if (isMovePossible(row, column) == false)
        {
           for (int i = 0; i < jTable1.getRowCount(); i++)
           {
               for (int j = 1; j < jTable1.getColumnCount(); j++)
                   jTable1.setValueAt("", i, j);
           }
           JOptionPane.showMessageDialog(rootPane, "Больше ходить некуда. Ваш результат : " + firstTaskCounter + " закрашенных клеток", "Конец игры", 2);
           firstTaskCounter = 0;
        }
        if (column != 0)
        {
            if (firstTaskCounter == 0)
            {
                jTable1.setValueAt("♘", row, column);
                fTlastRow = row;
                fTlastCol = column;
            }
            else 
            {
                if (trueMove(row, column))
                {
                   jTable1.setValueAt("♘", row, column);  
                   fTlastRow = row;
                   fTlastCol = column;
                }
            }
        }
        firstTaskCounter++;
    }//GEN-LAST:event_jTable1MouseClicked

