    private boolean trueMove(int row, int column)
    {
        if (jTable1.getValueAt(row, column).toString() != "")
            return false;
        if (row == fTlastRow + 1 && column == fTlastCol + 2)
            return true;
        if (row == fTlastRow + 1 && column == fTlastCol - 2)
            return true;
        if (row == fTlastRow - 1 && column == fTlastCol + 2)
            return true;
        if (row == fTlastRow - 1 && column == fTlastCol - 2)
            return true;
        if (row == fTlastRow + 2 && column == fTlastCol + 1)
            return true;
        if (row == fTlastRow + 2 && column == fTlastCol - 1)
            return true;
        if (row == fTlastRow - 2 && column == fTlastCol + 1)
            return true;
        if (row == fTlastRow - 2 && column == fTlastCol - 1)
            return true;

        return false;
    }
    
