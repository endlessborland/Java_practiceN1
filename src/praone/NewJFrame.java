    private void startButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButton4ActionPerformed
        int[][] array = new int[3][3];
        int sum = 0;
        for (int i = 0; i < 3; i++)
        {
            for (int j = 0; j < 3; j++)
            {
                if (jSqare4.getModel().getValueAt(i,j) == null)
                {
                    JOptionPane.showMessageDialog(rootPane, "Не должно быть пустых клеток");
                    return;
                }
                array[i][j] = Integer.parseInt(jSqare4.getModel().getValueAt(i, j).toString());
                if (array[i][j] == 0)
                {
                    JOptionPane.showMessageDialog(rootPane, "Не должно быть нулевых клеток");
                    return;
                }
                sum += array[i][j];
            }
        }
        for (int i = 0; i < 3; i++)
        {
            for (int j = 0; j < 3; j++)
            {
                for (int k = i; k < 3; k++)
                    for (int l = j; l < 3; l++)
                    {
                        if (i != k || l != j)
                            if (array[i][j] == array[k][l])
                            {
                                JOptionPane.showMessageDialog(rootPane, "Не должно быть повторов клеток");
                                return;
                            }
                    }
            }
        }
        int a = 0;
        for (int i = 0; i < 3; i++)
        {
            a += array[0][i] - 2 * array[1][i] + array[2][i];
            a += array[i][0] - 2 * array[i][1] + array[i][2];
        }
        a += array[0][0] + array [2][2] - array[0][2] - array[2][0];
        if (a == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Магический квадрат сошелся");
            return;
        }
        JOptionPane.showMessageDialog(rootPane, "не получилось");
        return;
    }//GEN-LAST:event_startButton4ActionPerformed

